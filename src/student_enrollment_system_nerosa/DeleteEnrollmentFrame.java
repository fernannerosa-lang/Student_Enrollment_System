    private void delStudentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delStudentBtnActionPerformed
    
    String idStr = IdField.getText();
    
    if (idStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields.");
        return;
    }
    
    EnrollmentDAO dao = new EnrollmentDAO();
    try {
            dao.deleteEnrollment(Integer.parseInt(idStr));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enrollment ID must be a valid number.");
            return;
        }
    GUI.refreshTable();
    this.dispose();
    
    }//GEN-LAST:event_delStudentBtnActionPerformed

